# inheritance
#    - the idea that one thing (a class) can inherit properties
#      (variables/functions/etc.) from another thing (a class)
#
# -----------------------------
#
# composition
#    - the idea that a class can be composed of other classes
#
# -----------------------------
#
# attribute
#    - properties (usually variables) that classes have, from composition
#
# -----------------------------
#
# is-a
#    - a phrase used to indicate inheritance (a bike 'is a' two-wheeler)
#
# -----------------------------
#
# has-a
#    - a phrase used to indicate composition (a two-wheeler 'has' two wheels)
